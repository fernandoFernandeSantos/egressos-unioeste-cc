    <p style="text-align: justify"> O curso de Informatica, no Campus de Cascavel, 
        foi implantado no ano de 1993. A resolução nº. 112/2009-CEPE de 27/07/09 
        altera o nome e o tempo de integralização do curso de Informática. 
        O nome do curso passa a para Ciência da Computação, a partir de 2010. 
        Funciona em período integral, ofertando anualmente 40 vagas. 
        O tempo mínimo para integralização é de 4 anos e o máximo de 7 anos, 
        com uma carga horária total de 3.750 horas, 
        das quais 180 horas são de atividades acadêmicas complementares. 
        O grau obtido é de Bacharel em Ciência da Computação. <p>
    
